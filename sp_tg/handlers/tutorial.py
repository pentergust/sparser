# –§—É–Ω–∫—Ü–∏–∏ –¥–ª—è —Å–±–æ—Ä–∫–∏ –∫–ª–∞–≤–∏–∞—Ç—É—Ä
# ============================

def get_tutorial_keyboard(page: int) -> InlineKeyboardMarkup:
    """–ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –º–Ω–æ–≥–æ—Å—Ç—Ä–∞–Ω–∏—á–Ω–æ–≥–æ –æ–±—É—á–µ–Ω–∏—è.

    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –ø–µ—Ä–µ–º–µ—â–µ–Ω–∏—è –º–µ–∂–¥—É —Å—Ç—Ä–∞–Ω–∏—Ü–∞–º–∏ –æ–±—É—á–µ–Ω–∏—è.
    –°–æ–¥–µ—Ä–∂–∏—Ç –∫–Ω–æ–ø–∫—É –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –∏ –∑–∞–∫—Ä—ã—Ç–∏—è —Å–ø—Ä–∞–≤–∫–∏.
    –ö–Ω–æ–ø–∫–∏ –ø–µ—Ä–µ–º–µ—â–µ–Ω–∏—è –Ω–∞ —Å–ª–µ–¥—É—é—â—É—é –∏ –ø—Ä–µ–¥—ã–¥—É—â—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—ã.
    –°–æ–¥–µ—Ä–∂–∞–Ω–∏–µ –¥–ª—è –±—ã—Å—Ç—Ä–æ–≥–æ –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —Å—Ç—Ä–∞–Ω–∏—Ü.

    Buttons:
        delete_msg => –£–¥–∞–ª–∏—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ.
        tutorual:{page} => –°–º–µ–Ω–∏—Ç—å —Å—Ç—Ä–∞–Ω–∏—Ü—É —Å–ø—Ä–∞–≤–∫–∏.

    Args:
        page (int): –¢–µ–∫—É—â–∞—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞ —Å–ø—Ä–∞–≤–∫–∏.

    Returns:
        InlineKeyboardMarkup: –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –¥–ª—è –ø–µ—Ä–µ–º–µ—â–µ–Ω–∏—è –ø–æ —Å–ø—Ä–∞–≤–∫–µ.
    
    """
    inline_keyboard = []

    # –ï—Å–ª–∏ —ç—Ç–æ –ø–µ—Ä–≤–∞—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞ -> –±–µ–∑ –∫–Ω–æ–ø–∫–∏ –Ω–∞–∑–∞–¥
    if page == 0:
        inline_keyboard.append([
            InlineKeyboardButton(text="üöÄ –ù–∞—á–∞—Ç—å", callback_data="tutorial:1")
        ])

    # –ö–Ω–æ–ø–∫–µ–∏ –¥–ª—è —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ø—Ä–æ—Å–º–æ—Ç—Ä–æ–º
    elif page != len(_TUTORIAL_MESSAGES)-1:
        inline_keyboard.append([
            InlineKeyboardButton(text="‚óÅ", callback_data=f"tutorial:{page-1}"),
            InlineKeyboardButton(
                text="üåü –î–∞–ª—å—à–µ", callback_data=f"tutorial:{page+1}"
            )
        ])

        # –ö—Ä–ø—Ç–∫–æ–µ —Å–æ–¥–µ—Ä–∂–∞–Ω–∏–µ –¥–ª—è –±—ã—Å—Ç—Ä–æ–≥–æ –ø–µ—Ä–µ–º–µ—â–µ–Ω–∏—è
        for i, x in enumerate(_TUTORIAL_MESSAGES[1:-1]):
            if i+1 == page:
                continue
            inline_keyboard.append([InlineKeyboardButton(
                text=x.splitlines()[0], callback_data=f"tutorial:{i+1}")]
            )

        inline_keyboard.append([InlineKeyboardButton(
            text="‚ùå –ó–∞–∫—Ä—ã—Ç—å", callback_data="delete_msg")]
        )

    # –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ –æ–±—É—á–µ–Ω–∏—è
    else:
        inline_keyboard.append([
            InlineKeyboardButton(
                text="üéâ –ó–∞–≤–µ—Ä—à–∏—Ç—å", callback_data="delete_msg"
            )
        ])

    return InlineKeyboardMarkup(inline_keyboard=inline_keyboard)


