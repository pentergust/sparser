def send_counter_message(sc: Schedule, counter: str, target: str) -> str:
    """–°–æ–±–∏—Ä–∞–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏ —Ä–∞–±–æ—Ç—ã —Å—á—ë—Ç—á–∏–∫–æ–≤.

    Counter: {cl, days, lessons, cabinets}
    Target: {cl, days, lessons, cabinets}
    Target: {main} –µ—Å–ª–∏ Counter in {lessons, cabinets}

    Args:
        sc (Schedule): –≠–∫–∑–µ–º–ø–ª—è—Ä —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è —É—Ä–æ–∫–æ–≤.
        counter (str): –ù–∞–∑–≤–∞–Ω–∏–µ —Ç–∏–ø–∞ —Å—á—ë—Ç—á–∏–∫–∞.
        target (str): –†–µ–∂–∏–º –ø—Ä–æ—Å–º–æ—Ç—Ä–∞ —Å—á—ë—Ç—á–∏–∫–∞.

    Returns:
        str: –°–æ–±—Ä–∞–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –æ—Ç —Å—á—ë—Ç—á–∏–∫–æ–≤.
    """
    intent = Intent.new()

    if counter == "cl":
        if target == "lessons":
            intent = Intent.construct(sc, cl=sc.cl)
        res = cl_counter(sc, intent)
    elif counter == "days":
        res = days_counter(sc, intent)
    elif counter == "lessons":
        res = index_counter(sc, intent)
    else:
        res = index_counter(sc, intent, cabinets_mode=True)

    groups = group_counter_res(res)
    message = f"‚ú® –°—á—ë—Ç—á–∏–∫ {counter}/{target}:"
    message += send_counter(groups, target=target)
    return message

